""" dynamical python """

# there are two type of programming languages we have 1. Static type (C, C++, JAVA) | 2. Dynamic type (Python, Javascript)

# static type -> (type needs to define like : int, float, boolean)
# int user_count = 500

# dynamic type -> (type will be taken dynamically)
USER_COUNT = 500

# we can get the type of python by a function called type()
print(type(USER_COUNT))
# output -> <class 'int'> -> it is saying that this variable is an integer
